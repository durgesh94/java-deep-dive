package corejava.variablesdatatypes;

public class PrimitiveDataTypes {
    public static void main(String[] args) {
        int age = 20;
        double pi = 3.14;
        char grade = 'A';
        byte b = 1;
        short s = 1;
        boolean isActive = false;
        float area = 124.59f;
        long distance = 1234;
        System.out.println(
            "Age: " + age +
            ", PI: " + pi +
            ", Grade: " + grade +
            ",b: " + b +
            ",s: " + s +
            ",isActive: " + isActive +
            ",area: " + area +
            ",distance: " + distance
        );
    }
}