package corejava.variablesdatatypes;

public class VariablesExample {
    public static void main(String[] args){
        // A variable is a named memory location that stores a value.
        // Instead of remembering the memory address, Java lets us use a meaningful name.
        // DataType VariableName = Value;
        String name = "Durgesh"; // String: DataType // name: Variable name // "Durgesh": Value assigned to variable
        int age = 25;
        double salary = 25000;
        boolean isPermanent = true;
        char grade = 'G';
        System.out.println("Name:"+name);
        System.out.println("Age:"+age);
        System.out.println("Salary:"+salary);
        System.out.println("Permanent:"+isPermanent);
        System.out.println("Grade:"+grade);
    }
}